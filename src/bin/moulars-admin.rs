#![deny(clippy::all)]
#![deny(clippy::pedantic)]

use anyhow::Context;
use clap::{Parser, Subcommand};
use hyper::http::Response;
use serde_derive::Deserialize;
use tracing_subscriber::{EnvFilter, filter::LevelFilter, prelude::*};
use ureq::Body;
use ureq::tls::{TlsConfig, TlsProvider};
use uuid::Uuid;

use moulars::api::{AccountInfoJson, AccountParams, ApiToken, OnlinePlayer};

// This tool is a client of the MOULArs API, so be sure to keep it in sync with
// additions and changes to src/api.rs

#[derive(Parser)]
#[command(name = "moulars-admin", version = "1.0", arg_required_else_help = true,
    about = "MOULArs Administration Helper")]
struct Args {
    #[command(subcommand)]
    command: Command,

    #[arg(short = 'U', long, help = "MOULArs API URL (default: http://localhost:14615/)")]
    api_url: Option<String>,

    #[arg(short = 'T', long, help = "API token for authorized operations")]
    api_token: Option<String>,
}

#[derive(Subcommand)]
enum Command {
    #[command(about = "Show the public encryption keys for use by clients, in server.ini format")]
    ClientKeys,

    #[command(about = "Initialize an empty MOULArs database with an administrative \
                       account and associated API token")]
    Initialize,

    #[command(about = "List all accounts in the server")]
    LsAccounts,

    #[command(about = "Show account details and API tokens for an account")]
    ShowAccount {
        #[arg(help = "Account UUID (default: use the account associated with the supplied API token)")]
        account: Option<Uuid>,
    },

    #[command(about = "Create a new account")]
    AddAccount {
        #[arg(help = "Account username")]
        username: String,

        #[arg(short, long, help = "Account password.  If not specified, will prompt interactively")]
        password: Option<String>,

        #[arg(short, long, value_delimiter = ',', help = "Set account flags (admin, banned, beta)")]
        flags: Vec<String>,
    },

    #[command(about = "Change account password")]
    SetPassword {
        #[arg(help = "Account UUID (default: use the account associated with the supplied API token)")]
        account: Option<Uuid>,

        #[arg(short, long, help = "New password.  If not specified, will prompt interactively")]
        password: Option<String>,
    },

    #[command(about = "Show online players")]
    ShowOnline,

    #[command(about = "Shut down the MOULArs server")]
    Shutdown,
}

struct ApiClient {
    agent: ureq::Agent,
    api_url: String,
    api_token: Option<String>,
}

fn main() {
    // Just print log messages generated by the moulars library
    tracing_subscriber::registry()
        .with(tracing_subscriber::fmt::layer()
                .without_time()
                .with_target(false))
        .with(EnvFilter::builder()
                .with_default_directive(LevelFilter::INFO.into())
                .from_env_lossy())
        .init();

    let args = Args::parse();

    // Command line arguments override environment variables for specifying
    // the API URL and auth token.
    let mut api_url = args.api_url
            .or_else(|| std::env::var("MOULARS_API_URL").ok())
            .unwrap_or_else(|| "http://localhost:14615".to_string());
    if api_url.ends_with('/') {
        let _ = api_url.pop();
    }
    let api_token = args.api_token.or_else(|| std::env::var("MOULARS_API_TOKEN").ok());

    let api_client = {
        let config = ureq::config::Config::builder()
            .tls_config(TlsConfig::builder()
                .provider(TlsProvider::NativeTls)
                .build())
            .http_status_as_error(false)
            .build();
        ApiClient {
            agent: config.new_agent(),
            api_url,
            api_token,
        }
    };

    let (result, action) = match args.command {
        Command::ClientKeys => (api_show_client_keys(&api_client), "show client keys"),
        Command::Initialize => (api_initialize(&api_client), "initialize admin account"),
        Command::LsAccounts => (api_ls_accounts(&api_client), "list accounts"),
        Command::ShowAccount { account } =>
            (api_show_account(&api_client, account), "fetch account details"),
        Command::AddAccount { username, password, flags } =>
            (api_add_account(&api_client, username, password, flags), "add account"),
        Command::SetPassword { account, password } =>
            (api_set_password(&api_client, account, password), "change account password"),
        Command::ShowOnline => (api_show_online(&api_client), "show online players"),
        Command::Shutdown => (api_shutdown(&api_client), "request server shutdown"),
    };
    if let Err(err) = result {
        eprintln!("Failed to {action}: {err}");
        std::process::exit(1);
    }
}

fn check_error(response: Response<Body>) -> anyhow::Result<Response<Body>> {
    let status_code = response.status();
    if status_code.is_success() {
        Ok(response)
    } else if status_code.is_client_error() {
        let body = response.into_body().read_to_string()?;
        let error: ApiError = serde_json::from_str(&body)
                        .with_context(|| "Failed to parse API error response")?;
        if status_code == 401 {
            Err(anyhow::anyhow!("{}.\nCheck that the provided API token is correct \
                                 and has sufficient privileges.", error.error))
        } else {
            Err(anyhow::anyhow!("{}", error.error))
        }
    } else if status_code.is_server_error() {
        Err(anyhow::anyhow!("Server error {status_code}.  Check the server logs for details."))
    } else {
        Err(anyhow::anyhow!("Unexpected HTTP status code: {status_code}"))
    }
}

fn api_show_client_keys(api: &ApiClient) -> anyhow::Result<()> {
    let response = api.agent.get(&format!("{}/client_keys", api.api_url)).call()?;
    let body = check_error(response)?.into_body().read_to_string()?;
    println!("{body}");
    Ok(())
}

fn api_initialize(api: &ApiClient) -> anyhow::Result<()> {
    let response = api.agent.get(&format!("{}/initialize", api.api_url)).call()?;
    let body = check_error(response)?.into_body().read_to_string()?;
    let api_response: InitializeResponse = serde_json::from_str(&body)
                            .with_context(|| "Failed to parse API response")?;

    println!("Administrative account initialized.");
    println!();
    println!("Username:   {}", api_response.username);
    println!("Account ID: {}", api_response.account_id);
    println!("API token:  {}", api_response.api_token);
    println!();
    println!("NOTE:  This API token cannot be retrieved again via the API, so take note of");
    println!("       it now.  It can be passed to the moulars-admin tool for authenticated");
    println!("       requests via the -T/--api-token option, or in the MOULARS_API_TOKEN");
    println!("       environment variable.");
    Ok(())
}

fn api_ls_accounts(api: &ApiClient) -> anyhow::Result<()> {
    let Some(api_token) = &api.api_token else {
        return Err(anyhow::anyhow!("API token is required for ls-accounts command"));
    };
    let response = api.agent.get(&format!("{}/accounts", api.api_url))
                            .header("Authorization", format!("Bearer {api_token}"))
                            .call()?;
    let body = check_error(response)?.into_body().read_to_string()?;
    let accounts: Vec<AccountInfoJson> = serde_json::from_str(&body)
                            .with_context(|| "Failed to parse API response")?;

    let max_username_len =
        usize::max(8, accounts.iter().map(|account| account.username.len()).max().unwrap_or(0));
    println!("{:<max_username_len$}  {:<36}  Flags", "Username", "Account ID");
    println!("{:<max_username_len$}  {:<36}  -----", "--------", "----------");
    for account in accounts {
        println!("{:<max_username_len$}  {:<36}  {}", account.username, account.account_id,
                 account.account_flags.join(", "));
    }
    Ok(())
}

fn api_show_account(api: &ApiClient, account: Option<Uuid>) -> anyhow::Result<()> {
    let Some(api_token) = &api.api_token else {
        return Err(anyhow::anyhow!("API token is required for show-account command"));
    };
    let request_uri = if let Some(uuid) = account {
        format!("{}/account?account={}", api.api_url, uuid)
    } else {
        format!("{}/account", api.api_url)
    };

    let response = api.agent.get(&request_uri)
                            .header("Authorization", format!("Bearer {api_token}"))
                            .call()?;
    let body = check_error(response)?.into_body().read_to_string()?;
    let account_details: AccountInfoJson = serde_json::from_str(&body)
                            .with_context(|| "Failed to parse API response")?;

    println!("Username:   {}", account_details.username);
    println!("Account ID: {}", account_details.account_id);
    if !account_details.account_flags.is_empty() {
        println!("Flags:      {}", account_details.account_flags.join(", "));
    }

    let request_uri = if let Some(uuid) = account {
        format!("{}/account/api_tokens?account={}", api.api_url, uuid)
    } else {
        format!("{}/account/api_tokens", api.api_url)
    };

    let response = api.agent.get(&request_uri)
                            .header("Authorization", format!("Bearer {api_token}"))
                            .call()?;
    let body = check_error(response)?.into_body().read_to_string()?;
    let api_tokens: Vec<ApiToken> = serde_json::from_str(&body)
                            .with_context(|| "Failed to parse API response")?;

    if !api_tokens.is_empty() {
        println!();
        println!("API tokens:");
        for token in api_tokens {
            println!("    {} : {}", token.token, token.comment);
        }
    }

    Ok(())
}

fn validate_flags(flags: &[String]) -> anyhow::Result<()> {
    const VALID_FLAGS: &[&str] = &["admin", "banned", "beta"];
    for flag in flags {
        if !VALID_FLAGS.contains(&flag.as_str()) {
            return Err(anyhow::anyhow!("Invalid account flag: {flag}"));
        }
    }
    Ok(())
}

fn get_password(password_arg: Option<String>) -> anyhow::Result<String> {
    if let Some(password) = password_arg { Ok(password) } else {
        let first = rpassword::prompt_password("Account password: ")?;
        let second = rpassword::prompt_password("Confirm password: ")?;
        if first == second {
            Ok(first)
        } else {
            Err(anyhow::anyhow!("Passwords do not match"))
        }
    }
}

fn api_add_account(api: &ApiClient, username: String, password: Option<String>,
                   flags: Vec<String>) -> anyhow::Result<()>
{
    let Some(api_token) = &api.api_token else {
        return Err(anyhow::anyhow!("API token is required for add-account command"));
    };

    validate_flags(&flags)?;
    let password = get_password(password)?;
    let params = AccountParams {
        username: Some(username),
        password: Some(password),
        account_flags: if flags.is_empty() { None } else { Some(flags) },
    };

    let response = api.agent.post(&format!("{}/account/new", api.api_url))
                            .header("Content-Type", "application/json")
                            .header("Authorization", format!("Bearer {api_token}"))
                            .send_json(&params)?;
    let body = check_error(response)?.into_body().read_to_string()?;
    let response: CreateAccountResponse = serde_json::from_str(&body)
                            .with_context(|| "Failed to parse API response")?;

    println!("Account created successfully.");
    println!("Account ID: {}", response.account_id);
    Ok(())
}

fn api_set_password(api: &ApiClient, account: Option<Uuid>, password: Option<String>)
    -> anyhow::Result<()>
{
    let Some(api_token) = &api.api_token else {
        return Err(anyhow::anyhow!("API token is required for set-password command"));
    };
    let request_uri = if let Some(uuid) = account {
        format!("{}/account/update?account={}", api.api_url, uuid)
    } else {
        format!("{}/account/update", api.api_url)
    };

    let password = get_password(password)?;
    let params = AccountParams {
        password: Some(password),
        ..Default::default()
    };

    let response = api.agent.post(&request_uri)
                            .header("Content-Type", "application/json")
                            .header("Authorization", format!("Bearer {api_token}"))
                            .send_json(&params)?;
    let _ = check_error(response)?;

    println!("Account password updated successfully.");
    Ok(())
}

fn api_show_online(api: &ApiClient) -> anyhow::Result<()> {
    let response = api.agent.get(&format!("{}/online", api.api_url)).call()?;
    let body = check_error(response)?.into_body().read_to_string()?;
    let players: Vec<OnlinePlayer> = serde_json::from_str(&body)
                            .with_context(|| "Failed to parse API response")?;

    println!("{} players currently online:", players.len());
    for player in players {
        println!("    {} ({})", player.name, player.location);
    }
    Ok(())
}

fn api_shutdown(api: &ApiClient) -> anyhow::Result<()> {
    let Some(api_token) = &api.api_token else {
        return Err(anyhow::anyhow!("API token is required for shutdown command"));
    };
    let response = api.agent.post(&format!("{}/shutdown", api.api_url))
                            .header("Authorization", format!("Bearer {api_token}"))
                            .send_empty()?;
    let _ = check_error(response)?;

    println!("Successfully requested server shutdown.");
    Ok(())
}

#[derive(Deserialize)]
struct ApiError {
    error: String,
}

#[derive(Deserialize)]
struct InitializeResponse {
    username: String,
    account_id: Uuid,
    api_token: String,
}

#[derive(Deserialize)]
struct CreateAccountResponse {
    //status: String,   // Ignored
    account_id: Uuid,
}
